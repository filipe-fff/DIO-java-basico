package edu.filipe.javaBasico.operadores.aula013;

public class AcrementoDecrementoBoleano {
    public static void main(String[] args)
    {
        int numero = 5;
        System.out.println(numero++);
        System.out.println(numero);
        System.out.println(++numero);

        boolean valor = true;
        System.out.println(!valor);
    }
}
