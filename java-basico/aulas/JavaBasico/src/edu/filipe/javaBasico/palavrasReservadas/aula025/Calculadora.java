/*
 * <h1>Calculadora</h1>
 *
 * <p> A <strong>Calculadora</strong> ela é um método que recebe os valores em parametros n1 e n2 para retornarem um valor que é a soma entre os dois. </p>
 *
 * <hr/>
 *
 *  @autor Filipe
 *  @version 1.0
 *  @since 16/08/2024
 */
public class Calculadora
{
    /*
     *  @param n1 primeiro número
     *  @param n2 segundo número
     *  @return retorna a soma entre os parametro n1 e n2
     *
     * */
    public int somar(int n1, int n2)
    {
        return n1+n2;
    }
}